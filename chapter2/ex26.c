#include <stdio.h>

int main (void) {

  int x=11, y=5;
  printf("Initial values| x:%d y:%d\n", x, y);
  printf("x & y = %d\n", x&y);
  printf("x | y = %d\n", x|y);
  printf("x ^ y = %d\n", x^y);
  printf("  ~x  = %d\n", ~x );
  return 0;
}
